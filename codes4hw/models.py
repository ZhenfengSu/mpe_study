class GetMask(autograd.Function):
    @staticmethod
    def forward(ctx, mask, b_mask):
        return b_mask

    @staticmethod
    def backward(ctx, g):
        # send the gradient g straight-through on the backward pass.
        return g, None

class Learnable_Gelu_hard(nn.Module):
    def __init__(self, device='cpu'):
        super(Learnable_Gelu_hard, self).__init__()
        
        self.device = device
        init_value = 0.0
        self.learn_mask = nn.Parameter(torch.tensor(init_value))
        self.b_mask = torch.ones(self.learn_mask.shape, requires_grad=False)
        
        self.flag = 1
        self.gelu = nn.GELU()
        
    def set_flag(self, flag):
        self.flag = flag
        if flag:
            self.b_mask = torch.ones(1, requires_grad=False)
        else:
            self.b_mask = torch.zeros(1, requires_grad=False)
    
    def forward(self, x):
        x_act = self.gelu(x)
        mask = GetMask.apply(self.learn_mask, self.b_mask).to(self.device)
        x = mask * x_act + (1 - mask) * x
        return x

class Learnable_Atten_hard(nn.Module):
    def __init__(self, attention=None ,device='cpu'):
        super(Learnable_Atten_hard, self).__init__()
        
        self.device = device
        init_value = 0.0
        self.learn_mask = nn.Parameter(torch.tensor(init_value))
        self.b_mask = torch.ones(self.learn_mask.shape, requires_grad=False)

        self.attention =attention
        self.flag = 1 
        
    def set_flag(self, flag):
        self.flag = flag
        if flag:
            self.b_mask = torch.ones(1, requires_grad=False)
        else:
            self.b_mask = torch.zeros(1, requires_grad=False)
    
    def forward(self, x):
        x_attn = self.attention(x)
        mask = GetMask.apply(self.learn_mask, self.b_mask).to(self.device)
        
        x = mask * x_attn + (1 - mask) * x
        return x
    
class Learnable_Block(nn.Module):
    def __init__(self, block=None, device='cpu'):
        super(Learnable_Block, self).__init__()
        
        self.device = device
        
        self.block = block
        self.act = nn.GELU()
        
    def forward(self, x):
        x = self.block(x)
        if self.block.mlp.act.flag:
            return x
        else:
            return self.act(x)
        
        
        
class DistilledVisionTransformer(VisionTransformer):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self.dist_token = nn.Parameter(torch.zeros(1, 1, self.embed_dim))
        num_patches = self.patch_embed.num_patches
        self.pos_embed = nn.Parameter(torch.zeros(1, num_patches + 2, self.embed_dim))
        self.head_dist = nn.Linear(self.embed_dim, self.num_classes) if self.num_classes > 0 else nn.Identity()

        trunc_normal_(self.dist_token, std=.02)
        trunc_normal_(self.pos_embed, std=.02)
        self.head_dist.apply(self._init_weights)

    def forward_features(self, x):
        # taken from https://github.com/rwightman/pytorch-image-models/blob/master/timm/models/vision_transformer.py
        # with slight modifications to add the dist_token
        B = x.shape[0]
        x = self.patch_embed(x)

        cls_tokens = self.cls_token.expand(B, -1, -1)  # stole cls_tokens impl from Phil Wang, thanks
        dist_token = self.dist_token.expand(B, -1, -1)
        x = torch.cat((cls_tokens, dist_token, x), dim=1)

        x = x + self.pos_embed
        x = self.pos_drop(x)

        for blk in self.blocks:
            x = blk(x)

        x = self.norm(x)
        return x[:, 0], x[:, 1]

    def forward(self, x):
        x, x_dist = self.forward_features(x)
        x = self.head(x)
        x_dist = self.head_dist(x_dist)
        if self.training:
            return x, x_dist
        else:
            # during inference, return the average of both classifier predictions
            return (x + x_dist) / 2


@register_model
def deit_tiny_patch16_224(pretrained=False, **kwargs):
    model = VisionTransformer(
        patch_size=16, embed_dim=192, depth=12, num_heads=3, mlp_ratio=4, qkv_bias=True,
        norm_layer=partial(nn.LayerNorm, eps=1e-6), **kwargs)
    model.default_cfg = _cfg()
    if pretrained:
        checkpoint = torch.hub.load_state_dict_from_url(
            url="https://dl.fbaipublicfiles.com/deit/deit_tiny_patch16_224-a1311bcf.pth",
            map_location="cpu", check_hash=True
        )
        model.load_state_dict(checkpoint["model"])
    return model


